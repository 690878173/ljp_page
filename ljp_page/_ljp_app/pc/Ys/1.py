import logging
from typing import List, Dict, Optional, Any

# 配置日志，方便调试解析错误
logging.basicConfig(level=logging.WARNING)
logger = logging.getLogger(__name__)


class MovieKey:
    """密钥信息封装类"""

    def __init__(self, method: str, uri: str|None = None, iv: bytes|None = None, **kwargs):
        self.method = method  # 加密方式（如AES-128）
        self.uri = uri  # 密钥地址
        self.iv = iv  # 初始向量（字节数组）
        self.extra = kwargs  # 其他参数（如KEYFORMAT）


class TsInfo:
    """TS片段信息封装类"""

    def __init__(self, url: str, duration: Optional[float] = None, sequence: int = 0, key_index: int = -1):
        self.url = url  # ts路径
        self.duration = duration  # 时长（秒）
        self.sequence = sequence  # 序号（基于EXT-X-MEDIA-SEQUENCE）
        self.key_index = key_index  # 关联的密钥索引（-1表示无加密）


def parse_m3u8(m3u8_text: str) -> tuple[List[TsInfo], List[MovieKey], bool]:
    """
    解析m3u8文本，提取ts片段信息、密钥列表和是否为点播（含ENDLIST）

    返回值：
        ts_list: TsInfo对象列表（含路径、时长、序号等）
        keys: MovieKey对象列表（所有密钥信息）
        is_vod: 是否为点播（True表示含#EXT-X-ENDLIST）
    """
    # 处理可能的BOM头（如UTF-8 BOM）
    m3u8_text = m3u8_text.lstrip('\ufeff')
    lines = [line.strip() for line in m3u8_text.splitlines()]  # 去除每行首尾空格

    ts_list: List[TsInfo] = []
    keys: List[MovieKey] = []
    is_vod = False  # 是否为点播（有#EXT-X-ENDLIST）

    # 解析过程中的临时变量
    current_duration: Optional[float] = None  # 上一个#EXTINF的时长
    media_sequence = 0  # ts起始序号（默认0）
    current_key_index = -1  # 当前生效的密钥索引（-1表示无加密）

    for line in lines:
        if not line:  # 跳过空行
            continue

        # 处理以#开头的标签
        if line.startswith('#'):
            # 解析#EXT-X-KEY（密钥信息）
            if line.startswith('#EXT-X-KEY:'):
                key_attrs = _parse_key_line(line)
                if key_attrs:
                    keys.append(MovieKey(**key_attrs))
                    current_key_index = len(keys) - 1  # 更新当前密钥索引
                continue

            # 解析#EXTINF（ts时长）
            if line.startswith('#EXT-X-INFO:'):
                # 兼容可能的拼写错误（正确标签是#EXTINF）
                logger.warning("可能的标签错误：#EXT-X-INFO，应为#EXTINF")
                line = line.replace('#EXT-X-INFO:', '#EXTINF:')
            if line.startswith('#EXTINF:'):
                duration_str = line.split(':', 1)[1].split(',')[0].strip()
                try:
                    current_duration = float(duration_str)
                except ValueError:
                    logger.warning(f"无效的#EXTINF时长：{duration_str}，忽略该值")
                    current_duration = None
                continue

            # 解析#EXT-X-MEDIA-SEQUENCE（ts起始序号）
            if line.startswith('#EXT-X-MEDIA-SEQUENCE:'):
                seq_str = line.split(':', 1)[1].strip()
                try:
                    media_sequence = int(seq_str)
                except ValueError:
                    logger.warning(f"无效的#EXT-X-MEDIA-SEQUENCE：{seq_str}，使用默认值0")
                continue

            # 解析#EXT-X-ENDLIST（点播标识）
            if line == '#EXT-X-ENDLIST':
                is_vod = True
                continue

            # 其他标签（如#EXT-X-TARGETDURATION）可根据需求扩展
            continue

        # 非#开头的行：ts路径
        ts_info = TsInfo(
            url=line,
            duration=current_duration,
            sequence=media_sequence,
            key_index=current_key_index
        )
        ts_list.append(ts_info)

        # 更新序号和临时变量
        media_sequence += 1
        current_duration = None  # 重置时长，等待下一个#EXTINF

    return ts_list, keys, is_vod


def _parse_key_line(key_line: str) -> Optional[Dict[str, Any]]:
    """辅助函数：解析#EXT-X-KEY行，返回密钥参数字典"""
    try:
        # 提取属性部分（去除#EXT-X-KEY:前缀）
        attr_part = key_line.split(':', 1)[1].strip()
        attrs = [attr.strip() for attr in attr_part.split(',') if attr.strip()]

        key_dict: Dict[str, Any] = {}
        for attr in attrs:
            if '=' not in attr:
                # logger.warning(f"无效的密钥属性：{attr}，跳过")
                continue
            # 分割为name和value（只分一次，避免value含=）
            name, value = attr.split('=', 1)
            name = name.strip().lower()  # 转为小写，统一处理
            value = value.strip()

            # 去除value的引号（双引号或单引号）
            if (value.startswith('"') and value.endswith('"')) or (value.startswith("'") and value.endswith("'")):
                value = value[1:-1]

            # 处理IV（转换0x前缀的十六进制为字节数组）
            if name == 'iv' and value.startswith('0x'):
                try:
                    key_dict['iv'] = bytes.fromhex(value[2:])  # 0x1234 → 字节数组b'\x12\x34'
                except ValueError:
                    # logger.warning(f"无效的IV格式：{value}，保留原始值")
                    key_dict[name] = value
            else:
                key_dict[name] = value

        # 检查必要参数（METHOD是必须的）
        if 'method' not in key_dict:
            # logger.warning("密钥缺少METHOD参数，忽略该密钥")
            return None
        return key_dict

    except Exception as e:
        print(f"解析密钥行失败：{key_line}，错误：{str(e)}")
        return None



